import com.baomidou.mybatisplus.core.metadata.IPage;
import com.baomidou.mybatisplus.core.metadata.OrderItem;
import com.baomidou.mybatisplus.extension.plugins.pagination.Page;
import com.danbro.common.enums.Constant;
import com.danbro.common.enums.PageParam;
import com.danbro.common.enums.pms.SQLFilter;

/**
 * 查询参数
 *
 * @author Mark sunlightcs@gmail.com
 */
public class Query<T> {

    public IPage<T> getPage(PageParam<T> params) {
        return this.getPage(params, null, false);
    }

    public IPage<T> getPage(PageParam<T> params, String defaultOrderField, boolean isAsc) {
        //分页参数
        long curPage = 1;
        long limit = 10;

        if (params.getPage() != null) {
            curPage = params.getPage();
        }
        if (params.getLimit() != null) {
            limit = params.getLimit();
        }

        //分页对象
        Page<T> page = new Page<>(curPage, limit);

        //分页参数
        params.setPageObject(page);

        //排序字段
        //防止SQL注入（因为sidx、order是通过拼接SQL实现排序的，会有SQL注入风险）
        String orderField = SQLFilter.sqlInject((String) params.getSidx());
        String order = params.getOrder();


        //前端字段排序
        if (MyStrUtils.isNotEmpty(orderField) && MyStrUtils.isNotEmpty(order)) {
            if (Constant.ASC.equalsIgnoreCase(order)) {
                return page.addOrder(OrderItem.asc(orderField));
            } else {
                return page.addOrder(OrderItem.desc(orderField));
            }
        }

        //没有排序字段，则不排序
        if (!MyStrUtils.isBlank(defaultOrderField)) {
            return page;
        }

        //默认排序
        if (isAsc) {
            page.addOrder(OrderItem.asc(defaultOrderField));
        } else {
            page.addOrder(OrderItem.desc(defaultOrderField));
        }

        return page;
    }
}
